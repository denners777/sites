				<h2>Glossário</h2>
				<p><strong>Aférese (Hemaférese)</strong><br>
					Processo de remoção de certos componentes do sangue de um doador, restituindo-lhe os componentes não necessários. Plaquetas, glóbulos vermelhos, glóbulos brancos ou plasma podem ser removidos separadamente. Esta técnica também é utilizada para remover células-tronco da circulação, de forma que estas possam ser congeladas, armazenadas e utilizadas posteriormente, ao invés de células-tronco da medula óssea, para um transplante.</p>
				<p><strong>Cateter</strong><br>
					Vários tipos de cateteres (Hickman, Broviac e outros) são utilizados em pacientes que recebem quimioterapia intensiva e/ou apoio nutricional. Um cateter implantado é um tubo especial inserido em uma veia importante do organismo, na porção superior do peito. A extremidade exposta do cateter pode ser utilizada para injeção de medicamentos, líquidos ou produtos do sangue, ou para retirar amostras sangüíneas. Com cuidados adequados os cateteres podem permanecer posicionados por longos períodos de tempo (muitos meses), se necessário.</p>
				<p><strong>Células-Tronco</strong><br>
					Células primitivas da medula, importantes para a produção de glóbulos vermelhos, glóbulos brancos e plaquetas (veja Hematopoese). Geralmente as células-tronco são encontradas abundantemente na medula, porém, algumas saem da mesma e circulam no sangue. Através de técnicas especiais, as células-tronco do sangue podem ser coletadas, preservadas por congelamento e, posteriormente, descongeladas e utilizadas no tratamento.</p>
				<p><strong>Citogenética</strong><br>
					Processo de análise do número e do formato dos cromossomos celulares. Além de identificar alterações nos cromossomos, os genes específicos afetados podem ser identificados em alguns casos. Essas descobertas são muito úteis para o diagnóstico e tratamento de tipos específicos de leucemia e linfoma</p>
				<p><strong>Crista Ilíaca</strong><br>
					Borda do quadril (bacia), local de onde normalmente é retirada a amostra de medula óssea para o diagnóstico de doenças nas células sanguíneas.</p>
				<p><strong>Criopreservação</strong><br>
					Trata-se da técnica utilizada para manter as células congeladas intactas e funcionais por muitos anos. Doença do Enxerto Contra o Hospedeiro Trata-se de um ataque dos linfócitos na medula do doador ou na suspensão das células do sangue (o enxerto) contra os tecidos do receptor (o hospedeiro). Os principais locais prejudicados são a pele, o fígado e a região gastrointestinal. Existem formas de prevenir e tratar a doença evitando ou controlando assim estes efeitos.</p>
				<p><strong>Enxerto</strong><br>
					É o processo que envolve as células-tronco transplantadas residentes na medula do receptor e a produção de células sangüíneas. Esta ocorrência é primeiramente evidente quando os novos glóbulos brancos, glóbulos vermelhos e plaquetas começam a aparecer no sangue do receptor após o transplante.</p>
				<p><strong>Hematopoese</strong><br>
					Processo de formação de células do sangue na medula. As células mais primitivas¬ da medula são as células-tronco. Elas iniciam o processo de desenvolvimento das células do sangue. As células-tronco se transformam em células sangüíneas jovens ou imaturas, como os glóbulos brancos ou vermelhos, de vários tipos. Este processo é denominado &quot;diferenciação&quot;. As células sangüíneas jovens se transformam posteriormente em células sangüíneas totalmente funcionais. Este processo é chamado &quot;maturação&quot;. As células saem então da medula e penetram no sangue e na circulação.</p>
				<p><strong>HLA</strong><br>
					Abreviatura de &quot;antígenos de leucócitos humanos&quot;. Estas proteínas se encontram na superfície da maioria das células dos tecidos e fazem com que cada indivíduo tenha um tipo característico de tecido. O teste de antígenos HLA é conhecido como &quot;tipagem do tecido&quot;. Em um teste de compatibilidade, as características do doador e do receptor são comparadas.</p>
				<p><strong>Hospedeiro</strong><br>
					É o receptor do transplante que está agindo como o hospedeiro para as células-tronco transplantadas.</p>
				<p><strong>Imunossupressão</strong><br>
					Estado no qual o sistema imunológico não funciona apropriadamente e suas funções protetoras se apresentam inadequadas. O paciente fica mais suscetível a infecções, inclusive por micróbios, que não são geralmente muito infecciosos. A imunossupressão pode ocorrer como resultado de quimioterapia intensiva ou radioterapia, principalmente quando utilizadas para condicionar o paciente para um transplante. Também pode ocorrer devido a estados de doença. Infusão de Células-Tronco Autólogas Esta técnica, freqüentemente denominada transplante, compreende a coleta de células da medula ou do sangue de um paciente. Estas células contêm células-tronco que são congeladas para uso posterior. Após terapia intensiva, o paciente recebe as células-tronco, reinfundidas através de um</p>
				<p><strong>Medula Óssea</strong><br>
					Os ossos são ocos e sua cavidade central é ocupada pela medula, um tecido esponjoso que desempenha papel fundamental no desenvolvimento das células sangüíneas. Após a puberdade, a medula dos ossos como coluna cervical, vértebras, costelas, esterno, pelve, ombros e crânio continuam ativos na formação de células sangüíneas.</p>
				<p><strong>Pancitopenia</strong><br>
					Diminuição abaixo do normal da concentração dos três principais tipos de células sangüíneas: glóbulos vermelhos, glóbulos brancos e plaquetas.</p>
				<p><strong>Petéquias</strong><br>
					Pequeninos pontos de sangramento na pele. Este tipo de sangramento resulta de uma baixa contagem de plaquetas. Estes pequenos pontos hemorrágicos são freqüentemente encontrados nas pernas, pés, tórax e braços. Desaparecem gradualmente quando a contagem de plaquetas aumenta.</p>
				<p><strong>Quimioterapia</strong><br>
					Uso de substância química (drogas ou medicamentos) para eliminar células malignas. Inúmeras drogas foram desenvolvidas com este objetivo; a maioria atua causando danos ao DNA das células. Como as células da medula, do trato intestinal, da pele e dos folículos de cabelo são mais sensíveis a estas drogas, efeitos colaterais nestes órgãos são comuns na quimioterapia; por exemplo, feridas na boca e perda de cabelo.</p>
				<p><strong>Rescidiva ou Recaída</strong><br>
					Trata-se de um retorno da doença após ela ter estado em remissão seguindo o tratamento. Regime de Condicionamento O tratamento condicionador é a terapia intensiva a base de drogas citóxicas ou com drogas e radiação de corpo inteiro antes do transplante de células-tronco. A terapia serve para três propósitos. O primeiro é que ela deprime seriamente os linfócitos que são as células-chave no sistema imunológico. Sua ação auxilia a prevenir a rejeição ao enxerto de células-tronco. Segundo, ela diminui acentuadamente as células da medula. Isto pode ser importante de forma a desobstruir os locais especiais em que se abrigam as células-tronco transplantadas para enxertar. Terceiro, se o paciente estiver sendo transplantado devido a uma doença do sangue, esta terapia intensiva servirá para reduzir o número de células remanescentes da doença.</p>
				<p><strong>Remissão</strong><br>
					Desaparecimento completo de uma doença, geralmente como resultado do tratamento. Os termos &quot;completa&quot; ou &quot;parcial&quot; são utilizados para modificar o termo remissão. Remissão completa significa que não existe mais nenhuma evidência da doença. Remissão parcial significa que o tratamento provoca uma melhora acentuada, porém ainda há evidências residuais da doença.</p>
				<p><strong>Tolerância</strong><br>
					É um acontecimento muito importante no sucesso do transplante a longo prazo. Após um certo tempo, geralmente cerca de um ano, os linfócitos T prévios do hospedeiro morrem, e novos linfócitos são formados a partir das células-tronco do doador que foram enxertadas. Elas &quot;adaptam-se&quot; ao novo hospedeiro e param de atacar as células do receptor.</p>
				<p><strong>Transplante de Células-Tronco</strong><br>
					Técnica desenvolvida para restaurar a medula gravemente lesada de um paciente. Especificamente, o produto transplantado é uma fração muito pequena das células da medula denominadas células-tronco. Estas células-tronco não somente se localizam na medula como também circulam no sangue. Elas podem ser coletadas do sangue de um doador através do uso de um agente ou agentes que provocam a liberação de um grande número das mesmas no sangue, onde são coletadas através de aférese. As células-tronco circulam em grandes números no sangue do feto e também podem ser obtidas do sangue, da placenta ou do cordão umbilical após o nascimento. A coleta, congelamento e armazenamento de &quot;sangue do cordão umbilical&quot; proporcionam uma fonte alternativa de células-tronco para transplantes. Como tanto o sangue como a medula são ótimas fontes de células para transplante, o termo &quot;transplante de medula óssea&quot; tem sido substituído pelo termo &quot;transplante de células-tronco&quot; para estes procedimentos.<br>
					Se o doador é um gêmeo idêntico, o transplante é chamado &quot;singênico&quot;, termo médico que significa geneticamente idêntico. Se o doador não é um gêmeo idêntico, o transplante é chamado &quot;alogênico&quot;, indicando que é da mesma espécie e, na prática, quase sempre compatível com o tipo tissular. O termo &quot;compatível não relacionado&quot; é aplicado a doadores recrutados a partir de Registros de Doadores voluntários. A técnica de coleta da medula de um paciente, congelando-a e devolvendo-a ao mesmo após quimioterapia ou radioterapia intensiva é chamada de &quot;transplante autólogo&quot; ou &quot;autotransplante&quot;.</p>
